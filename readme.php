Nom du projet
Projet de Réservation de Billets de Voyage

Description
Le projet de réservation de billets de voyage est une application web développée pour la plateforme Windows.
 Cette solution permet aux utilisateurs de rechercher, réserver et gérer leurs billets de voyage de manière conviviale.
L'application utilise les langages HTML, CSS, PHP, ainsi que la base de données MySQL via XAMPP.
La conception du système a été réalisée en suivant la méthode MERISE.

Fonctionnalités
Recherche de Billets : Les utilisateurs peuvent rechercher des billets en fonction de différents critères tels que la destination, la date, etc.
Réservation de Billets : Les utilisateurs peuvent réserver des billets pour les trajets sélectionnés avec une interface conviviale.
Gestion des Réservations : Un tableau de bord permet aux utilisateurs de gérer leurs réservations, y compris la modification et l'annulation.
Détails des Voyages : Les détails complets des voyages sont affichés,la création, l'affichage, la mise à jour et la suppression de billets

Configuration et Installation
Prérequis
Assurez-vous d'avoir installé les éléments suivants sur votre machine Windows :
XAMPP pour le serveur Apache et MySQL.
Assurez-vous d'avoir PHP 8.2.10, 10.4.28-MariaDB et Apache/2.4.56  (ou un serveur web équivalent) installés sur votre machine.,4.,
Configuration de la Base de Données
Apres installation de XAMPP et démarrez les services Apache et MySQL.
Configurez les paramètres de base de données dans le fichier config.php.

Configuration du Projet
Clonez le dépôt : git clone https://github.com/mamebassine/application_gestion_billets
Accédez au répertoire du projet : cd projet-reservation-billets
Lancement de l'Application
Installez les dépendances : npm install
Démarrez le serveur : npm start
Accès à l'Application
Ouvrez votre navigateur et accédez à http://localhost/application_gestion_billets.

Utilisation
Recherche et Réservation :
Utilisez la barre de recherche pour trouver des billets selon vos critères.
Sélectionnez un trajet et procédez à la réservation.
Gestion des Réservations :
Consultez et gérez vos réservations depuis le tableau de bord.
